package L5.Hw2;


public class Author {
    public String name;
    public String surname;
    public String birthday;
    public String country;

    public String toString() {
        return "Author{" +
                "name='" + name + '\'' +
                ", surname='" + surname + '\'' +
                ", birthday=" + birthday +
                ", country='" + country + '\'' +
                '}';
    }
}
